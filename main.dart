class MyHomePage extends StatefulWidget {
  const MyHomePage({Key? key}) : super(key: key);

  @override
  State<MyHomePage> createState() => _MyHomePageState();
}

class _MyHomePageState extends State<MyHomePage> {
  LocationData? _locationData;

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: const Text("Location"),
      ),
      body: Center(
        child: Column(
          mainAxisSize: MainAxisSize.min,
          children: [
            ElevatedButton(onPressed: _determinePosition, child: Text('Determine Position')),
            SizedBox(height: 20.0,),
            Text(_locationData!=null? 'Long: ${_locationData!.longitude}, Lat: ${_locationData!.latitude}'
                : '----',),
          ],
        ),
      ),
    );
  }

  void _determinePosition() async {
    Location location = Location();

    bool _serviceEnabled;
    PermissionStatus _permissionGranted;

    _serviceEnabled = await location.serviceEnabled();
    if (!_serviceEnabled) {
      _serviceEnabled = await location.requestService();
      if (!_serviceEnabled) {
        print("!_serviceEnabled");
        return;
      }
    }

    _permissionGranted = await location.hasPermission();
    if (_permissionGranted == PermissionStatus.denied) {
      _permissionGranted = await location.requestPermission();
      if (_permissionGranted != PermissionStatus.granted) {
        print("_permissionGranted != PermissionStatus.granted");
        return;
      }
    }

    _locationData = await location.getLocation();
    print(_locationData);

    /// update ui
    setState(() {});


    /// get continuous location updates
    // location.onLocationChanged.listen((LocationData currentLocation) {
    //   // Use current location
    // });

    /// receive location when application is in background
    // location.enableBackgroundMode(enable: true);
  }

}

